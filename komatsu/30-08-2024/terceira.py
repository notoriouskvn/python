# Exemplo 3 - Conjuntos (Sets)

frutas = {"Banana", "Maçã", "Pêra", "Morango"}
vegetais = {"Beterraba", "Pepino", "Alface", "Rúcula", "Brócolis"}
print("Lista original:", frutas)
print("Lista original:", vegetais)
frutas.add("Pêssego") #adicionando
print("Depois de adicionar um item:", frutas)
frutas.remove("Pêra") #removendo
vegetais.remove("Pepino") #removendo
print("Depois de remover itens:")
print(frutas)
print(vegetais)
